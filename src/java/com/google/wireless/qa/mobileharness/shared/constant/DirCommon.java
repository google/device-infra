package com.google.wireless.qa.mobileharness.shared.constant;

import com.google.devtools.mobileharness.shared.util.flags.Flags;

/** Definition of the common file directories. */
public final class DirCommon {

  /** The default root dir of the temp directory. */
  public static final String DEFAULT_TMP_DIR = "/usr/local/google/mobileharness";

  /** Prefix of all the directories generated by Mobile Harness servers. */
  public static final String DEFAULT_FOLDER_PREFIX = "mh_";

  /** Default file name pattern of the log files. */
  public static final String DEFAULT_LOG_FILE_NAME = "log%g.txt";

  /** See {@link GenDir#KEY_NAME_OF_TEST_GEN_FILE_DIR_RELATIVE_PATH}. */
  public static final String KEY_NAME_OF_TEST_GEN_FILE_DIR_RELATIVE_PATH =
      GenDir.KEY_NAME_OF_TEST_GEN_FILE_DIR_RELATIVE_PATH;

  /** Returns the public directory of the Apache/GSE. */
  public static String getPublicDirRoot() {
    return Flags.instance().publicDir.getNonNull();
  }

  /** Returns the root of the temporary directory. */
  public static String getTempDirRoot() {
    return Flags.instance().tmpDirRoot.getNonNull();
  }

  private DirCommon() {}
}
