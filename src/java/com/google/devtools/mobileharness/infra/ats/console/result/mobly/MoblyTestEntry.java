package com.google.devtools.mobileharness.infra.ats.console.result.mobly;

import com.google.auto.value.AutoValue;
import com.google.common.collect.ImmutableList;
import com.google.devtools.mobileharness.infra.ats.console.result.proto.ResultProto.MoblyResult;
import com.google.errorprone.annotations.CanIgnoreReturnValue;
import java.util.Optional;

/**
 * A general container class for a document that contains the result of a single Mobly test.
 *
 * <p>This class should mirror the Mobly class at {@code records.py#TestResultRecord}. Any changes
 * made to the Mobly class should be mirrored here carefully.
 */
@AutoValue
public abstract class MoblyTestEntry implements MoblyYamlDocEntry {

  @Override
  public Type getType() {
    return Type.RECORD;
  }

  public abstract String getTestName();

  public abstract String getTestClass();

  /** A unique identifier of the test entry, generated by Mobly. */
  public abstract Optional<String> getSignature();

  /** Result of test given from Mobly */
  public abstract MoblyResult getResult();

  /** Start time of test in milliseconds since epoch */
  public abstract Optional<Long> getBeginTime();

  /** End time of test in milliseconds since epoch */
  public abstract Optional<Long> getEndTime();

  /** Any details given from test. */
  public abstract Optional<String> getDetails();

  /** Stacktrace of error if test had an error of failed. */
  public abstract Optional<String> getStacktrace();

  /** Additional info that users have piped into an assertion. */
  public abstract Optional<String> getExtras();

  /**
   * The signature of the previous iteration of this retry.
   *
   * <p>Only set for retry iterations. Parsers can use this field to construct the chain of
   * execution for each retried test.
   */
  public abstract Optional<String> getRetryParent();

  public abstract Optional<String> getUid();

  /** Termination signal type for the test. */
  public abstract Optional<String> getTerminationSignalType();

  /**
   * List of extra errors for a given test. If no extra errors exist, an empty list will be
   * returned.
   */
  public abstract ImmutableList<ExtraError> getExtraErrors();

  public static Builder builder() {
    return new AutoValue_MoblyTestEntry.Builder();
  }

  /** MoblyTestEntry Builder class. */
  @AutoValue.Builder
  public abstract static class Builder {
    public abstract Builder setTestName(String value);

    public abstract Builder setTestClass(String value);

    public abstract Builder setSignature(String value);

    public abstract Builder setBeginTime(long value);

    public abstract Builder setEndTime(long value);

    public abstract Builder setResult(MoblyResult value);

    public abstract Builder setDetails(String value);

    public abstract Builder setStacktrace(String value);

    public abstract Builder setRetryParent(String value);

    public abstract Builder setExtras(String value);

    public abstract Builder setUid(String value);

    public abstract Builder setTerminationSignalType(String value);

    protected abstract ImmutableList.Builder<ExtraError> extraErrorsBuilder();

    @CanIgnoreReturnValue
    public Builder addExtraError(ExtraError value) {
      extraErrorsBuilder().add(value);
      return this;
    }

    public abstract MoblyTestEntry build();
  }

  /** A container class for the extra errors produced from a Mobly test run. */
  @AutoValue
  public abstract static class ExtraError {
    /** Test 'position' (test method) where error occurred. */
    public abstract String getPosition();

    public abstract Optional<String> getDetails();

    public abstract Optional<String> getExtras();

    public abstract Optional<String> getStacktrace();

    public static Builder builder() {
      return new AutoValue_MoblyTestEntry_ExtraError.Builder();
    }

    /** ExtraError Builder class. */
    @AutoValue.Builder
    public abstract static class Builder {
      public abstract Builder setPosition(String value);

      public abstract Builder setDetails(String value);

      public abstract Builder setExtras(String value);

      public abstract Builder setStacktrace(String value);

      public abstract ExtraError build();
    }
  }
}
