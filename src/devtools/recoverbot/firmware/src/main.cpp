// ============================================================================
// RecoverBot Firmware
// Target: M5Stack Core3 (ESP32) + PCA9685 servo boards.
//
// What this firmware does (feature list)
// ----------------------------------------------------------------------------
// 1) Board auto-detection
//    - Scans I2C addresses 0x40 ~ 0x47 on Wire1 (Core3 Port A)
//    - For each detected PCA9685 board, initializes it at 50Hz servo frequency
//
// 2) Per-servo calibration (persisted in ESP32 NVS / Preferences)
//    - Each (boardAddr, channel) can have calibrated pressTick + releaseTick
//    - Stored as a packed uint32: (pressTick<<16) | releaseTick
//    - Key format: "c_<ADDR>_<CH>" e.g. "c_40_00"
//    - On boot: loads calibrations for all detected boards/channels
//    - On boot: sets every channel to its releaseTick (calibrated if present)
//
// 3) Safe "press then auto-release" servo actuation
//    - press_ms <boardHex> <ch> <ms>  : press for ms then auto-release
//    - press    <boardHex> <ch>       : press using max duration cap
//    - release  <boardHex> <ch>       : go to releaseTick immediately
//    - A safety cap FORCE_MAX_PRESS_MS prevents "stuck press" forever
//
// 4) RAW tick mode (guarded, timed unlock)
//    - raw on [sec]   : enable RAW mode for N seconds (default 300s)
//    - raw off        : disable RAW mode immediately
//    - raw_tick <boardHex> <ch> <tick>
//        Writes a tick value directly, but ONLY when RAW is enabled
//        AND tick is within [g_rawTickMin, g_rawTickMax] (configurable)
//    - RAW mode auto-locks after expiration
//
// 5) Serial command interface
//    - Type commands over Serial @115200
//    - "help" prints all available commands
//
// 6) Dual logging: Serial + on-device screen
//    - logMsg(...) prints the same line to Serial and the Core3 display
//    - Screen is cleared when switching from the idle logo back to logs
//
// 7) Idle logo screensaver (optional UI)
//    - If no "event" happens for IDLE_SHOW_LOGO_MS (default 30s),
//      the firmware shows the embedded PNG logo.
//    - While the logo is shown:
//        * Any new log line will automatically return to log screen
//        * A screen tap will return to log screen
//
// Notes / Assumptions
// ----------------------------------------------------------------------------
// - PCA9685 servo frequency is set to 50Hz.
// - "tick" means PCA9685 PWM OFF count [0..4095] within one 20ms period.
// - This firmware intentionally keeps UI simple (no scrollback buffer,
//   no timestamps on-screen, no gesture scroll).
//
// Dependencies (Arduino Library Manager)
// ----------------------------------------------------------------------------
// - M5Unified (by M5Stack)
// - Adafruit PWM Servo Driver Library (Adafruit_PWMServoDriver.h)
// - Adafruit BusIO (required by Adafruit PWM Servo Driver Library)
// ============================================================================


// Embedded logo data (from logo.h generated by build.sh)
// This makes sure the firmware always has the latest logo data.
#define LOGO_DATA logo_240_png
#define LOGO_LEN  logo_240_png_len

// PNG is 240x240.
static const int LOGO_W = 240;
static const int LOGO_H = 240;

// ===== Safety cap for forced press =====
const uint32_t FORCE_MAX_PRESS_MS = 100000UL;  // 100 seconds

// ===== RAW tick guard rails =====
static uint32_t g_rawUnlockWindowMs = 300000UL;  // default 5 minutes
static uint16_t g_rawTickMin        = 250;       // safe lower bound
static uint16_t g_rawTickMax        = 450;       // safe upper bound

bool     g_rawEnabled = false;
uint32_t g_rawExpire  = 0;   // 0 => no expiry

// ===== Default Servo PWM presets (ticks) =====
// If a channel has no saved calibration, these defaults are used.
const int POWER_SERVO_PRESS         = 360;
const int POWER_SERVO_RELEASE       = 300;
const int VOLUME_UP_SERVO_PRESS     = 360;
const int VOLUME_UP_SERVO_RELEASE   = 300;
const int VOLUME_DOWN_SERVO_PRESS   = 360;
const int VOLUME_DOWN_SERVO_RELEASE = 300;

const int MAX_CHANNELS = 16;
const int MAX_BOARDS   = 8;   // max boards 0x40..0x47

Adafruit_PWMServoDriver* pwmBoards[MAX_BOARDS];
uint8_t boardAddresses[MAX_BOARDS];
uint8_t numBoards = 0;

// Servo active state (for auto-release)
struct ServoState {
  bool     active = false;
  uint32_t releaseTime = 0;
};
ServoState servoStates[MAX_BOARDS][MAX_CHANNELS];

// Debug: last tick written (not required for functionality)
uint16_t lastTicks[MAX_BOARDS][MAX_CHANNELS];

// Per-servo calibration storage (in RAM)
// If g_cal[b][ch].has == true, press/release ticks override defaults.
struct CalEntry {
  bool     has = false;
  uint16_t pressTick = 0;
  uint16_t releaseTick = 0;
};
CalEntry g_cal[MAX_BOARDS][MAX_CHANNELS];

Preferences prefs;  // NVS namespace: "recoverbot"


// ---------------------------------------------------------
// Idle logo control (simple "screensaver")
// ---------------------------------------------------------
static const uint32_t IDLE_SHOW_LOGO_MS = 30000UL;
static uint32_t g_lastEventMs = 0;
static bool g_logoShown = false;

// Treat any interaction or servo action as an "event" to reset idle timer.
static inline void markEvent() {
  g_lastEventMs = millis();
}


// ---------------------------------------------------------
// UI helpers
// ---------------------------------------------------------
void showLogScreenHeader() {
  // Hard clear the screen to avoid any leftovers from PNG alpha blending
  M5.Display.fillScreen(TFT_BLACK);
  M5.Display.setCursor(0, 0);
  M5.Display.setTextColor(TFT_GREEN, TFT_BLACK);
  M5.Display.println("RecoverBot");
  M5.Display.setTextColor(TFT_WHITE, TFT_BLACK);
}

void showLogoScreen() {
  // Center the logo
  int x = (M5.Display.width()  - LOGO_W) / 2;
  int y = (M5.Display.height() - LOGO_H) / 2;
  if (x < 0) x = 0;
  if (y < 0) y = 0;

  // Clear entire screen first
  M5.Display.fillScreen(TFT_WHITE);

  // Also clear logo area in case PNG has transparent pixels
  M5.Display.fillRect(x, y, LOGO_W, LOGO_H, TFT_WHITE);

  // Draw PNG from embedded byte array
  M5.Display.drawPng(LOGO_DATA, LOGO_LEN, x, y);

  g_logoShown = true;
}

// Tap while logo shown => return to logs
void handleTouchToExitLogo() {
  if (!g_logoShown) return;

  int32_t x, y;
  if (M5.Display.getTouch(&x, &y)) {
    g_logoShown = false;
    markEvent();
    showLogScreenHeader();
    M5.Display.println("Back to logs (tap)");
  }
}


// ---------------------------------------------------------
// Logging: Serial + Screen (screen always stays coherent)
// ---------------------------------------------------------
void logMsg(const char* format, ...) {
  // If logo is currently shown, switch back to log screen first.
  // NOTE: Do this BEFORE markEvent() so we don't lose the state.
  if (g_logoShown) {
    g_logoShown = false;
    showLogScreenHeader();
  }

  markEvent();

  char buf[192];
  va_list args;
  va_start(args, format);
  vsnprintf(buf, sizeof(buf), format, args);
  va_end(args);

  Serial.println(buf);
  M5.Display.println(buf);
}


// ---------------------------------------------------------
// PCA9685 / servo helpers
// ---------------------------------------------------------
bool isPCA9685Present(uint8_t addr) {
  Wire1.beginTransmission(addr);
  return Wire1.endTransmission() == 0;
}

int getBoardIndex(uint8_t boardAddr) {
  for (int i = 0; i < (int)numBoards; ++i) {
    if (boardAddresses[i] == boardAddr) return i;
  }
  return -1;
}

bool isTickSafe(uint16_t t) {
  return (t >= g_rawTickMin && t <= g_rawTickMax);
}

void getDefaultPressRelease(uint8_t channel, int& pressPwm, int& releasePwm) {
  // Example mapping by channel%4
  if (channel % 4 == 0) {
    pressPwm = POWER_SERVO_PRESS;
    releasePwm = POWER_SERVO_RELEASE;
  } else if (channel % 4 == 1) {
    pressPwm = VOLUME_UP_SERVO_PRESS;
    releasePwm = VOLUME_UP_SERVO_RELEASE;
  } else if (channel % 4 == 2) {
    pressPwm = VOLUME_DOWN_SERVO_PRESS;
    releasePwm = VOLUME_DOWN_SERVO_RELEASE;
  } else {
    // reserved / fallback
    pressPwm = POWER_SERVO_PRESS;
    releasePwm = POWER_SERVO_RELEASE;
  }
}

void buildCalKey(char* out, size_t cap, uint8_t boardAddr, uint8_t ch) {
  // Key example: "c_40_00"
  snprintf(out, cap, "c_%02X_%02u", boardAddr, ch);
}


// ---------------------------------------------------------
// Calibration load/save (NVS)
// ---------------------------------------------------------
void loadOneCal(uint8_t boardAddr, uint8_t ch) {
  int b = getBoardIndex(boardAddr);
  if (b < 0 || ch >= MAX_CHANNELS) return;

  char key[16];
  buildCalKey(key, sizeof(key), boardAddr, ch);

  // Stored as uint32: (press<<16) | release. 0 => not set.
  uint32_t packed = prefs.getUInt(key, 0);
  if (packed == 0) {
    g_cal[b][ch].has = false;
    return;
  }

  uint16_t pressT = (uint16_t)(packed >> 16);
  uint16_t relT   = (uint16_t)(packed & 0xFFFF);

  // Reject out-of-policy data
  if (!isTickSafe(pressT) || !isTickSafe(relT)) {
    g_cal[b][ch].has = false;
    return;
  }

  g_cal[b][ch].has = true;
  g_cal[b][ch].pressTick = pressT;
  g_cal[b][ch].releaseTick = relT;
}

void loadAllCalForDetectedBoards() {
  for (int b = 0; b < (int)numBoards; ++b) {
    for (int ch = 0; ch < MAX_CHANNELS; ++ch) {
      loadOneCal(boardAddresses[b], static_cast<uint8_t>(ch));
    }
  }
}

void saveOneCal(uint8_t boardAddr, uint8_t ch, uint16_t pressT, uint16_t relT) {
  char key[16];
  buildCalKey(key, sizeof(key), boardAddr, ch);
  uint32_t packed = ((uint32_t)pressT << 16) | (uint32_t)relT;
  prefs.putUInt(key, packed);
}

void deleteOneCal(uint8_t boardAddr, uint8_t ch) {
  char key[16];
  buildCalKey(key, sizeof(key), boardAddr, ch);
  prefs.remove(key);

  int b = getBoardIndex(boardAddr);
  if (b >= 0 && ch < MAX_CHANNELS) g_cal[b][ch].has = false;
}

void getPressAndReleaseTicks(uint8_t boardAddr, uint8_t channel,
                             uint16_t& pressT, uint16_t& relT) {
  int b = getBoardIndex(boardAddr);
  if (b >= 0 && channel < MAX_CHANNELS && g_cal[b][channel].has) {
    pressT = g_cal[b][channel].pressTick;
    relT   = g_cal[b][channel].releaseTick;
    return;
  }

  int dp, dr;
  getDefaultPressRelease(channel, dp, dr);
  pressT = (uint16_t)dp;
  relT   = (uint16_t)dr;
}

void loadGlobalConfig() {
  g_rawUnlockWindowMs = prefs.getUInt("cfg_win", 300000UL);
  g_rawTickMin        = prefs.getUShort("cfg_min", 250);
  g_rawTickMax        = prefs.getUShort("cfg_max", 450);
}

void saveGlobalConfig() {
  prefs.putUInt("cfg_win", g_rawUnlockWindowMs);
  prefs.putUShort("cfg_min", g_rawTickMin);
  prefs.putUShort("cfg_max", g_rawTickMax);
}

void resetGlobalConfig() {
  prefs.remove("cfg_win");
  prefs.remove("cfg_min");
  prefs.remove("cfg_max");
  g_rawUnlockWindowMs = 300000UL;
  g_rawTickMin        = 250;
  g_rawTickMax        = 450;
}

// ---------------------------------------------------------
// Core servo actions
// ---------------------------------------------------------
void applyTicks(int boardIndex, uint8_t channel, uint16_t ticks) {
  pwmBoards[boardIndex]->setPWM(channel, 0, ticks);
  lastTicks[boardIndex][channel] = ticks;

  // Servo activity counts as an event (prevents idle logo during active usage)
  markEvent();
}

void releaseServo(uint8_t boardAddr, uint8_t channel) {
  if (channel >= MAX_CHANNELS) { logMsg("ERR: invalid channel"); return; }
  int index = getBoardIndex(boardAddr);
  if (index < 0) { logMsg("ERR: board not found"); return; }

  uint16_t pressT, relT;
  getPressAndReleaseTicks(boardAddr, channel, pressT, relT);

  applyTicks(index, channel, relT);
  servoStates[index][channel].active = false;

  logMsg("OK,RELEASE,0x%02X,ch=%u", boardAddr, channel);
}

void pressServo(uint8_t boardAddr, uint8_t channel, uint32_t duration) {
  if (channel >= MAX_CHANNELS) { logMsg("ERR: invalid channel"); return; }
  int index = getBoardIndex(boardAddr);
  if (index < 0) { logMsg("ERR: board not found"); return; }

  // Safety cap
  if (duration > FORCE_MAX_PRESS_MS) duration = FORCE_MAX_PRESS_MS;

  uint16_t pressT, relT;
  getPressAndReleaseTicks(boardAddr, channel, pressT, relT);

  applyTicks(index, channel, pressT);
  servoStates[index][channel].active = true;
  servoStates[index][channel].releaseTime = millis() + duration;

  logMsg("OK,PRESS,0x%02X,ch=%u,ms=%lu,tick=%u", boardAddr, channel,
         duration, pressT);
}

void checkAndReleaseServos() {
  uint32_t now = millis();
  for (int b = 0; b < (int)numBoards; ++b) {
    for (int c = 0; c < MAX_CHANNELS; ++c) {
      if (servoStates[b][c].active && now >= servoStates[b][c].releaseTime) {
        uint16_t pressT, relT;
        getPressAndReleaseTicks(boardAddresses[b], static_cast<uint8_t>(c),
                                pressT, relT);
        applyTicks(b, static_cast<uint8_t>(c), relT);
        servoStates[b][c].active = false;
        logMsg("AUTO_RELEASE,0x%02X,ch=%d", boardAddresses[b], c);
      }
    }
  }
}

void setRawTicks(uint8_t boardAddr, uint8_t channel, uint16_t ticks) {
  if (channel >= MAX_CHANNELS) { logMsg("ERR: invalid channel"); return; }
  int index = getBoardIndex(boardAddr);
  if (index < 0) { logMsg("ERR: board not found"); return; }

  // Auto-lock RAW if expired
  if (!g_rawEnabled || (g_rawExpire && millis() > g_rawExpire)) {
    g_rawEnabled = false;
    logMsg("ERR: raw disabled");
    return;
  }

  if (!isTickSafe(ticks)) {
    logMsg("ERR: raw tick out of range [%u,%u]", g_rawTickMin, g_rawTickMax);
    return;
  }

  applyTicks(index, channel, ticks);
  servoStates[index][channel].active = false;

  logMsg("OK,RAW_TICK,0x%02X,ch=%u,tick=%u", boardAddr, channel, ticks);
}


// ---------------------------------------------------------
// Status / Help
// ---------------------------------------------------------
void printStatus() {
  uint32_t now = millis();
  logMsg("=== Status ===");
  logMsg("Boards: %u", numBoards);
  logMsg("RAW Config: win=%lu ms, min=%u, max=%u",
         g_rawUnlockWindowMs, g_rawTickMin, g_rawTickMax);

  if (g_rawEnabled) {
    if (g_rawExpire) {
      int32_t remain = (int32_t)(g_rawExpire - now);
      logMsg("RAW: ON (expires in %d ms)", remain);
    } else {
      logMsg("RAW: ON (no expiry)");
    }
  } else {
    logMsg("RAW: OFF");
  }

  int activeCount = 0;
  for (int b = 0; b < (int)numBoards; ++b)
    for (int c = 0; c < MAX_CHANNELS; ++c)
      if (servoStates[b][c].active) activeCount++;

  logMsg("Active servos: %d", activeCount);
  for (int b = 0; b < (int)numBoards; ++b) {
    for (int c = 0; c < MAX_CHANNELS; ++c) {
      if (servoStates[b][c].active) {
        int32_t remain = (int32_t)(servoStates[b][c].releaseTime - now);
        logMsg(" - 0x%02X ch%d remaining=%d ms", boardAddresses[b], c,
               remain);
      }
    }
  }
  logMsg("=============");
}

