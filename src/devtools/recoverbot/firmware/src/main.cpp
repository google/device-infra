// Command table
struct Cmd { const char* name; void (*fn)(int, char**); };

const Cmd g_cmds[] = {
  { "ping",      cmd_ping      },
  { "status",    cmd_status    },
  { "help",      cmd_help      },
  { "press_ms",  cmd_press_ms  },
  { "press",     cmd_press     },
  { "release",   cmd_release   },
  { "raw_tick",  cmd_raw_tick  },
  { "raw_on",    cmd_raw_on    },
  { "raw_off",   cmd_raw_off   },
  { "cal_set",   cmd_cal_set   },
  { "cal_get",   cmd_cal_get   },
  { "cal_reset", cmd_cal_reset },
  { "cal_list",  cmd_cal_list  },
  { "cfg_set",   cmd_cfg_set   },
  { "cfg_get",   cmd_cfg_get   },
  { "cfg_reset", cmd_cfg_reset },
};
const size_t g_cmdCount = sizeof(g_cmds) / sizeof(g_cmds[0]);

void dispatchCommand(char* line) {
  // Auto-lock RAW if expired
  if (g_rawEnabled && g_rawExpire && millis() > g_rawExpire) {
    g_rawEnabled = false;
  }

  char* argv[12];
  int argc = tokenize(line, argv, 12);
  if (!argc) return;

  // Any command input is an event
  markEvent();

  // Allow "raw on/off" form (user convenience)
  if (strcmp(argv[0], "raw") == 0) {
    if (argc >= 2 && strcmp(argv[1], "on") == 0) {
      if (argc >= 3) {
        char* fakeArgv[2] = { (char*)"raw_on", argv[2] };
        cmd_raw_on(2, fakeArgv);
      } else {
        char* fakeArgv[1] = { (char*)"raw_on" };
        cmd_raw_on(1, fakeArgv);
      }
      return;
    } else if (argc >= 2 && strcmp(argv[1], "off") == 0) {
      cmd_raw_off(0, nullptr);
      return;
    } else {
      logMsg("ERR: raw [on [sec] | off]");
      return;
    }
  }

  // Lookup normal commands
  for (size_t i = 0; i < g_cmdCount; ++i) {
    if (strcmp(argv[0], g_cmds[i].name) == 0) {
      g_cmds[i].fn(argc, argv);
      return;
    }
  }

  logMsg("ERR: unknown cmd: %s", argv[0]);
}


// ---------------------------------------------------------
// Arduino setup/loop (Core3)
// ---------------------------------------------------------
void setup() {
  auto cfg = M5.config();
  M5.begin(cfg);  // Core3 power management / screen init
  M5.Display.setRotation(1);
  M5.Display.setTextSize(1);

  Serial.begin(115200);
  delay(200);

  // Initialize idle timer baseline
  g_lastEventMs = millis();
  g_logoShown = false;

  showLogScreenHeader();
  M5.Display.println("RecoverBot booting...");

  // Core3 Port A I2C: SDA=2, SCL=1
  Wire1.begin(2, 1, 100000);
  delay(500);

  // Open NVS namespace
  prefs.begin("recoverbot", false);

  // Detect PCA9685 boards on Wire1
  for (uint8_t addr = 0x40; addr <= 0x47 && numBoards < MAX_BOARDS; ++addr) {
    if (!isPCA9685Present(addr)) continue;

    uint8_t idx = numBoards;

    // Adafruit library expects TwoWire& (reference), NOT pointer
    pwmBoards[idx] = new Adafruit_PWMServoDriver(addr, Wire1);
    boardAddresses[idx] = addr;

    // Init arrays for this board
    for (int c = 0; c < MAX_CHANNELS; ++c) {
      servoStates[idx][c].active = false;
      servoStates[idx][c].releaseTime = 0;
      lastTicks[idx][c] = 0;
      g_cal[idx][c].has = false;
    }

    // Increment numBoards BEFORE loading calibration
    numBoards++;

    pwmBoards[idx]->begin();
    pwmBoards[idx]->setPWMFreq(50);

    logMsg("Found Board: 0x%02X", addr);
  }

  if (numBoards == 0) {
    M5.Display.setTextColor(RED, TFT_BLACK);
    logMsg("ERROR: No PCA9685 boards detected on Wire1 (Port A)!");
    return;
  }

  // Load global configuration
  loadGlobalConfig();

  // Load calibration for detected boards
  loadAllCalForDetectedBoards();

  // Set all channels to RELEASE position (calibrated if exists else default)
  for (int b = 0; b < (int)numBoards; ++b) {
    for (int ch = 0; ch < MAX_CHANNELS; ++ch) {
      uint16_t pressT, relT;
      getPressAndReleaseTicks(boardAddresses[b], static_cast<uint8_t>(ch),
                                pressT, relT);
      applyTicks(b, static_cast<uint8_t>(ch), relT);
    }
  }

  M5.Display.setTextColor(TFT_GREEN, TFT_BLACK);
  logMsg("System Ready. Boards=%u. Type 'help'.", numBoards);
}

void loop() {
  M5.update();

  // Tap screen to exit logo (only active while logo is shown)
  handleTouchToExitLogo();

  // Serial command input (one line per command)
  static char line[180];
  if (readLine(line, sizeof(line))) {
    dispatchCommand(line);
  }

  // Periodic release checks (auto-release when time is up)
  checkAndReleaseServos();

  // Idle => show logo once and keep it until an event occurs (or tap)
  uint32_t now = millis();
  if (!g_logoShown && (now - g_lastEventMs) >= IDLE_SHOW_LOGO_MS) {
    showLogoScreen();
  }

  delay(1);
}
